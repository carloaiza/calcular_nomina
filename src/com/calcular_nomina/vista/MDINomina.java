    private void llenarEmpleados()
    {
        ///Extrae el modelo de la tabla (Columnas y filas)
        DefaultTableModel model = (DefaultTableModel) tblEmpleados.getModel();
        //Limpia toda la tabla
        model.getDataVector().removeAllElements();
        //Declara un arreglo de objetos para representar una fila de la tabla
        //Object[] fila = {"75147235","Carlos Loaiza",2000000,false};
        //Adiciona la fila a la tabla
        //model.addRow(fila);
        //Refresca la tabla
        
        //Recorriendo el controlador de empleados y llenando mi tabla
        for(Empleado emp: controlNomina.getEmpleados())
        {
            if(emp!=null)
            {
                Object[] fila = {emp.getCedula(),emp.getNombre(),
                emp.getSalario(),emp.isLicencia()};
                model.addRow(fila);
            }
        }
        
        tblEmpleados.repaint();
        
    }
    
    
    public void controlarEstadoMenus()
    {       
        // Si ya se presentó una autenticación
        mnuListarEmpleados.setEnabled(false);
        mnuCrearEmpleado.setEnabled(false);
        mnuCalcularNomina.setEnabled(false);
        
        mnuCalcularNomina.setVisible(false);
        mnuListarEmpleados.setVisible(false);
        mnuCrearEmpleado.setVisible(false);
        
        if(usuarioAutenticado!=null)
        {
            //Evaluar el rol
            switch(usuarioAutenticado.getRol().getCodigo())
            {
                case 1:
                    mnuListarEmpleados.setEnabled(true);
                    mnuCrearEmpleado.setEnabled(true);
                    mnuCalcularNomina.setEnabled(true);
                    
                    mnuListarEmpleados.setVisible(true);
                    mnuCrearEmpleado.setVisible(true);
                    mnuCalcularNomina.setVisible(true);
                    break;
                case 2:
                    mnuListarEmpleados.setEnabled(true);
                    mnuListarEmpleados.setVisible(true);
                    break;
            }
        }
        
        
         
    }
    

