            ///Es porque todo está bien
            controlarEstadoMenus();
            jifLogin.hide();             
            JOptionPane.showMessageDialog(rootPane,"Bienvenido "
                    +usuarioAutenticado.getNombre(),"Ingreso Exitoso",1);
            
